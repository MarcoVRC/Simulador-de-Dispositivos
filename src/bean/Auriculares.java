package bean;

/**
 *
 * @author RC
 */
public class Auriculares {
    private String nombre;
    private String dispositivoAsociado;
    private String correoAsociado;
    private String encendido;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getDispositivoAsociado() {
        return dispositivoAsociado;
    }

    public void setDispositivoAsociado(String dispositivoAsociado) {
        this.dispositivoAsociado = dispositivoAsociado;
    }

    public String getCorreoAsociado() {
        return correoAsociado;
    }

    public void setCorreoAsociado(String correoAsociado) {
        this.correoAsociado = correoAsociado;
    }

    public String getEncendido() {
        return encendido;
    }

    public void setEncendido(String encendido) {
        this.encendido = encendido;
    }
    
    
}
